// Given the Principal Amount, Interest Percentage and Time calculate the simple interest amount
var principal_amount=1000000;
var percentage=2.5;
var tenure=18;
var simple_interest=principal_amount*percentage*tenure;

console.log("Principal amount :",principal_amount);
console.log("Interest rate :",percentage);
console.log("Duration :",tenure,"months");
console.log("Simple interest :",simple_interest);